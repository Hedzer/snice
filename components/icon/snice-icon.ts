// Global custom icon registry
const customIcons: Record<string, string> = {};

@element('snice-icon')
export class SniceIcon extends HTMLElement implements SniceIconElement {
  @property()
  name = '';

  @property()
  src = '';

  @property()
  size: IconSize | number = 'medium';

  @property()
  color = '';

  @property()
  label = '';

  static registerIcon(name: string, svg: string) {
    customIcons[name] = svg;
  }

  static registerIcons(icons: Record<string, string>) {
    for (const [name, svg] of Object.entries(icons)) {
      customIcons[name] = svg;
    }
  }

  static getIcon(name: string): string | undefined {
    return customIcons[name] || BUILTIN_ICONS[name];
  }

  static getRegisteredNames(): string[] {
    return [...Object.keys(BUILTIN_ICONS), ...Object.keys(customIcons)];
  }

  @render()
  renderContent() {
    const ariaLabel = this.label || this.name || '';
    const ariaHidden = !this.label;

    if (this.src) {
      return html/*html*/`
        <div class="icon" part="base"
             role="${this.label ? 'img' : 'presentation'}"
             aria-hidden="${ariaHidden}">
          <img src="${this.src}" alt="${ariaLabel}">
          <if ${this.label}>
            <span class="sr-only">${this.label}</span>
          </if>
        </div>
      `;
    }

    const svgContent = this.getSvgContent();
    return html/*html*/`
      <div class="icon" part="base"
           role="${this.label ? 'img' : 'presentation'}"
           aria-hidden="${ariaHidden}"
           aria-label="${ariaLabel}">
        ${unsafeHTML(svgContent)}
      </div>
    `;
  }

  @styles()
  componentStyles() {
    return css/*css*/`${cssContent}`;
  }

  @ready()
  init() {
    this.updateCustomSize();
    this.updateColor();
  }

  @watch('size')
  updateCustomSize() {
    if (typeof this.size === 'number' || (typeof this.size === 'string' && !isNaN(Number(this.size)) && (this.size as string) !== '')) {
      const px = Number(this.size);
      this.style.width = `${px / 16}rem`;
      this.style.height = `${px / 16}rem`;
    } else {
      this.style.removeProperty('width');
      this.style.removeProperty('height');
    }
  }

  @watch('color')
  updateColor() {
    if (this.color) {
      this.style.color = this.color;
    } else {
      this.style.removeProperty('color');
    }
  }

  private getSvgContent(): string {
    if (!this.name) return '';
    const svg = customIcons[this.name] || BUILTIN_ICONS[this.name];
    return svg || '';
  }
}
